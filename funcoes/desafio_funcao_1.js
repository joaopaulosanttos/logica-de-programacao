// função: 2 params -> console.log(+)
// Uma dica! Não é permitido começar im params com um número ex. (1n, 2n) NÃO É PERMITIDO!
function somar(n1, n2) {
    console.log(n1 + n2);
}
// função: 2 params -> console.log(-)
function subtrair(a, b) { 
    console.log(a - b);
}


somar(10, 5);
somar(10, 25);

subtrair(20, 15);
subtrair(25, 10);

