// Atraves do typeof sabemos o tipo da variavel!!  
let idade = 31; 
console.log(typeof 31);
console.log(typeof idade); 

let salario = 4800.80; // O javascript não faz distinção entre números interios e decimais. Todos são number! 
console.log(typeof salario);

// O valor boolean representa os valores verdadeiros ou falsos! 
let estaChovendo = true; // ou false 
console.log(typeof estaChovendo); 

// String (Texto) --> Sequencia de Símbolos e Letras! 
console.log(typeof "Teste"); 
console.log(typeof 'Teste'); // a String funciona usando aspas duplas ou simples! 

// Atenção: Esses são os três tipos básicos suportados por javascript (Number, Boolean, String)! 