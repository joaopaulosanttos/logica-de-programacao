let a = 3; 

// Talvez queira alterar a variavel: 

a = a + 10; 

// e no final você quer imprimir o valor de "a"

console.log(a); 

// Quando você quer que um valor não seja alterado depois: 

const b = 4;  

// b = b + 10; Se fizer isso ocorrerar um erro, pois não poder alterar um valor de uma const 

console.log(b); 

// Atenção: Em um programa é mais recomendado o uso de const! 