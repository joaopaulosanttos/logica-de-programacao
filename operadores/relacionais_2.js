// Em javascript existe tipos dinâmicos!
let a = 1;
let b = "1";
let c = "1";

console.log(a == b);
console.log(a === b); // neste caso é para saber se "a" é estritamente igual a "b", ou seja, se são do mesmo tipo! 
console.log(c === b);

console.log(a != b); 
console.log(a !== b); // neste caso é para saber se "a" é estritamente igual a "b", ou seja, se são do mesmo tipo! 
console.log(c !== b);